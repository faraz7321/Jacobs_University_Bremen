#include <stdio.h>
int main()
{
    int x = 17, y = 4;
    printf("x=%d\n", x);                         //value of x
    printf("y=%d\n", y);                         //value of y
    printf("sum=%d\n", x + y);                   //sum of x and y
    printf("product=%d\n", x * y);               //product of x and y
    printf("difference=%d\n", x - y);            //difference of x and y
    printf("division=%f\n", (double)x / y);      //x divided by y
    printf("remainder of division=%i\n", x % y); //remainder of x divided by y
}