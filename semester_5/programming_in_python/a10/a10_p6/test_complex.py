from complex import Complex

c1 = Complex(2, 3)
c2 = Complex(1, -4)

print(f"Multiplication of {c1} and {c2} = {c1 * c2}")
print(f"Division of {c1} by {c2} = {c1 / c2}")
print(f"{c1} and {c2} equal? {c1 == c2}")
print(f"{c1} and {c2} not equal? {c1 != c2}")

